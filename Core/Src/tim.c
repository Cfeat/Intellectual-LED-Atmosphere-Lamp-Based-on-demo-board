{
	HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_4);
}

void KE1_Ext_PWM_Breathing_LED(int iLoopCnt)
{
	__HAL_TIM_SET_AUTORELOAD(&htim1, 370-1);
	HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_4);
	int duty = 370, dir = 0;
	do{
		HAL_Delay(5);
		if(dir) duty++;
		else duty--;

		if(370 == duty) dir=0;
		if(0 == duty) {dir=1; iLoopCnt--;}
		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, duty);

	}while(iLoopCnt);
	HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_4);
}

