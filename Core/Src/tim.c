void KE1_Ext_PWM_Start(unsigned short usFreq, unsigned char ucDuty)
{
	/*
	 ** Fpwm =Tclk / ((arr+1)*(psc+1))(å•ä½ï¼šHz)
	 ** 		arr æ˜¯è®¡æ•°å™¨
	 ** 		psc æ˜¯é¢„åˆ†é¢‘
	 **
	 ** arr = 32000000/32/usFreq
	 ** ccr = (ucDuty*arr)/100
	 ** */
	unsigned int arr = htim1.Init.Period+1, ccr = 0;
	if(0 != usFreq){
		arr = (32000000/(htim1.Init.Prescaler+1))/usFreq;
	}
	if(0 != ucDuty){
		ccr = (ucDuty*arr)/100;
	}

	//printf("pwm arr:%d, ccr:%d\r\n", arr, ccr);
	arr -= 1;

	__HAL_TIM_SET_AUTORELOAD(&htim1, arr);
	__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, ccr);

	HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_4);

}

